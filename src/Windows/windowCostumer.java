        CostumerRepositoryImp costomer = new CostumerRepositoryImp(entitiManage.getEntityManager());
        Costomer costumer = new Costomer();
        costumer.setName(jTextFieldName.getText());
        costumer.setRfc(jTextFieldRfc.getText());
        costumer.setAddress(jTextFieldAddress.getText());
        costumer.setPhone(jTextFieldPhone.getText());
        
        costomer.Save(costumer);
        costomer.commit();
